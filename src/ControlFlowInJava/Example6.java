package ControlFlowInJava;

public class Example6 {
    public static void main(String[] args) {
        //assignment operators
        int x;

        x = 10;
        System.out.println(x);

        //compound operators
        x += 3;
        System.out.println(x);

        x -= 5;
        System.out.println(x);

        x *= 3;
        System.out.println(x);

        x /= 6;
        System.out.println(x);


    }
}
